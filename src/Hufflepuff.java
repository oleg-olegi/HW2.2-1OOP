 class Hufflepuff extends Hogwarts {
     private int hardworking;
     private int loyal;//верность
     private int honest;//честность

     public Hufflepuff(int witchcraft, int transgression, int hardworking, int loyal, int honest) {
         super(witchcraft, transgression);
         this.hardworking = hardworking;
         this.loyal = loyal;
         this.honest = honest;
     }
 }
